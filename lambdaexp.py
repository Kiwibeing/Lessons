x = 10
square = x * x

# A function (lambda) with a formal parameter x, that returns the value of 'x * x'. This expression must be a single line
# Lambda functions cannot include any statements, such as while statements
# Basically, 'square' is being binded to the lambda statement
square = lambda x : x * x
# No need for a return keyword, this allows for a function to be written in a single expression

